import java.util.Scanner;

public class CodeUp1074 {

	public static void main(String[] args) {
		Scanner scan = new Scanner(System.in);
		int input = scan.nextInt();
		for(int i=0; i<input; input--) {
			if(input!=0) {
				System.out.println(input);
			} else if (input==1) {
				break;
			}
			
		}
	}
	
}
